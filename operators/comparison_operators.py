def number_comparison_operators():

    #Comparison Operators Exercise
    num1 = 20
    num2 = 10
    
    # Equal == x == y    
    print(num1==num2)

    # !=	Not equal	x != y
    print(num1!=num2)

    # >	Greater than	x > y
    print(num1>num2)

    # <	Less than	x < y
    print(num1<num2)

    # >=	Greater than or equal to	x >= y
    print(num1 >= num2)

    # <=	Less than or equal to	x <= y
    print(num1 <= num2)

if __name__=='__main__':
    number_comparison_operators()