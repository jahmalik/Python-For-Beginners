def basic_number_subtraction():

    # Int Subtraction
    num1 = 654
    num2 = 239
    print(f'Difference of {num1} and {num2} is: {num1-num2}')

    # Float Subtraction
    num3 = 75.25
    num4 = 28.48
    print(f'Difference of {num3} and {num4} is: {num3-num4: .2f}')

if __name__=='__main__':
    basic_number_subtraction()