def basic_number_division():
    num1 = 12
    num2 = 2
    print(f'Division of {num1} and {num2} is: {int(num1/num2)}')


    # Float Division
    num3 = 75.25
    num4 = 28.48
    print(f'Division of {num3} and {num4} is: {float(num3/num4): .2f}')

if __name__=='__main__':
    basic_number_division()