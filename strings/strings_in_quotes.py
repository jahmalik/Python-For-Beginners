def main():

    # String in single quote
    str1 = 'I am Usman'

    # String in double quotes
    str2 = "I live in Austin, Texas"

    # String in tripple quotes
    str3 = """I love to play ice hockey"""

    #printing string values one by one
    print(str1)
    print(str2)
    print(str3)

    #printing string values on a single line
    print(str1, str2, str3)

    print('Name:', str1)
    print('Place:', str2)
    print('Hobbies:', str3)

if __name__=='__main__':
    main()