def basic_number_addition():

    # Int Addition
    num1 = 654
    num2 = 239
    print(f'sum of {num1} and {num2} is: {num1+num2}')

    # Float Addition
    num3 = 75.25
    num4 = 28.48
    print(f'sum of {num3} and {num4} is: {num3+num4: .2f}')

if __name__=='__main__':
    basic_number_addition()

